import java.util.Scanner;

public class Main_11 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()) {
            Long a = sc.nextLong();
            Long b = sc.nextLong();
            System.out.println(a + b);
        }
    }
}
